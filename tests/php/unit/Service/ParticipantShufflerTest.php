    // Simple party with simple excludes
    public function testParticipantShufflerCase1()
    {
        $party = new Party();
        foreach ($party->getParticipants() as $defaultParticipant) {
            $party->removeParticipant($defaultParticipant);
        }

        $participant1 = new Participant();
        $participant1->setName('Participant 1');
        $party->addParticipant($participant1);

        $participant2 = new Participant();
        $participant2->setName('Participant 2');
        $party->addParticipant($participant2);

        $participant3 = new Participant();
        $participant3->setName('Participant 3');
        $party->addParticipant($participant3);

        $participant4 = new Participant();
        $participant4->setName('Participant 4');
        $party->addParticipant($participant4);

        $participant1->addExcludedParticipant($participant2);
        $participant2->addExcludedParticipant($participant3);
        $participant4->addExcludedParticipant($participant1);
        $participant4->addExcludedParticipant($participant2);

        $ParticipantShuffler = new ParticipantShuffler();
        for ($i = 0; $i < 1000; ++$i) {
            $shuffeledParticipants = $ParticipantShuffler->shuffleParticipants($party);

            $key = 0;
            foreach ($party->getParticipants() as $participant) {
                //check if we did not match excluded participant
                $this->assertNotContains($shuffeledParticipants[$key], $participant->getExcludedParticipants());
                //check if we have an Participant matched
                $this->assertNotNull($shuffeledParticipants[$key]);
                ++$key;
            }
        }
    }

